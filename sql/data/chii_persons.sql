1970年至1980年间，由于东京movie的社长藤冈丰在MADHOUSE成立时提供了资金援助，出崎统与杉野昭夫成为搭档，参与东映动画的电视剧动画制作，也成为“TMS”（东京Movie新社）动画制作的核心人物。出崎统与杉野昭夫独立并成立スタジオあんなぷる之后，丸山正雄就任社长，开始主要以林太郎监督、角川书店制作的剧场版动画及OVA作品。

1990年期间，接受了许多电视剧动画制作的总承包方的外包工作。1989年的《以柔克刚》作为开始，标志了MADHOUSE从90年代正式开展自己的总承包动画制作。之后与韩国制作公司DR MOVIE合作。开始委任统包工作。
从1998年开始播放的《魔卡少女樱》（浅香守生导演作品），以优美的脚本、演出、作画、音乐升华了原作，俘获了大批的粉丝。MADHOUSE因此受到了不同程度的好评。该做也成为MADHOUSE的代表作。
1999年12月，在《DNA²》、《炸弹人 弹珠人爆外传》等制作中吸收了有限公司パオハウス。有限公司マッド・ハウス把业务转移至パオハウス、パオハウス的商号变更为“株式会社マッド・ハウス”。パオハウス是《以柔克刚》的制片商Kitty film的落合茂一在1993年独立创立的公司，1999年落合茂一去世后，kitty group出身的増田弘道接管了其一年的代表。之后増田弘道担任有限公司MADHOUSE的代表董事职务，“有限会社マッド・ハウス”经过两次变更为“有限会社マッドボックス”。

进入21世纪，MADHOUSE集结了在世界上评价颇高的川尻善昭、今敏等动画人。从《黑客帝国动画版》等艺术性较高的作品到《银河天使》等商业性较高的作品，MADHOUSE不断开拓不同领域上的动画，从而进入成熟期。另外，MADHOUSE也参与到《千与千寻》等吉卜力工作室制作的动画中，从事原画协力。直到目前的2017年，在其他公司制作的动画电影中，MADHOUSE依然经常列为制作协力。
2001年得到DR MOVIE的投资。同年，与DR MOVIE共同制作的《爆旋陀螺》引入了数码喷漆与数码合成。之后的作品亦不断数码化。这是因为，20世纪90年代末期，整个日本动画行业在引进数码制作上仍是十分慎重，而到了2001年，尽管赛璐珞与胶片摄影仍然被普遍使用在动画制作上，但全体动画业界同时也在快速推进动画的数码制作化，所以MADHOUSE才决定引进数码技术。
2004年，手机内容的制作供应商、大型企业Index通过第三方定向增发，将MADHOUSE纳为其子公司。同年11月为了名称的统一与表记，将商号从“株式会社マッド・ハウス”变更为“株式会社マッドハウス”。MADHOUSE与当时Index旗下的TOMY COMPANY、Interchannel、T.Y.Entertainment、ATLUS（旧公司），以及当时TOMY COMPANY旗下的龙之子制作、与TOMY COMPANY进行业务合作的工合在线娱乐、该公司的子公司Broccoli，在MediaMix（媒介组合）模式上探讨过合作的可能性。
2005年，就深夜档等方面已经与日本电视台放送网、VAP、电通三家公司有业务关系。2006年接受WOWOW的第三方定向增发。另外，MADHOUSE还引进了当时传统制作一向没有重视的CG，与同为Index旗下的CG制作公司DYNAM Pictures合作，构建以往作画与CG融合的挑战，创作不同于其他公司所制作的动画的立体感的作品。

2010年之后，MADHOUSE出现经营问题。2010年8月24日，今敏在为下部作品《造梦机器》制作的途中因患上胰腺癌而去世，终年46岁，该动画电影至今仍然无法完成。同年9月10日，日本振兴银行经营破产，与日本振兴银行及其融资和债权购买等活动关系颇深的母公司Index受到严重的经济损失，影响到Index内的各个组成公司。此外，MADHOUSE也面临了连续两年的赤字。
2011年2月8日，日本电视台放送网宣布收购MADHOUSE，并在同月18日通过承担约十亿日元的用于第三方定向增发金额，代替Index成为占股84.5%的最大股东。同时日本电视台放送网出身的冈田浩行就任MADHOUSE新社长。原社长丸山正雄则于2011年6月从MADHOUSE退职，带走部分员工并成立MAPPA。之后的2012年3月，MADHOUSE从有限公司变更为股份有限公司，正式成为日本电视台放送网的旗下公司。此事反映出日本动画制作产业及制作公司在经济能力上的虚弱与低迷，而MADHOUSE为应对这样现状，接受了日本电视台放送网所具有的内容管理能力的技术与经验，着力改善经营状况。
2011年以后，MADHOUSE多次参与到日本电视台播放的深夜动画的制作，直到2015年9月的《俺物语!!》放送完毕之后，才再多次制作在其他电视台播放的动画。近年来，担当以漫画为原作的动画制作，也负责《魔法科高校的劣等生》与《NO GAME NO LIFE 游戏人生》等以轻小说为原作的动画制作。
2022年，宣布于本社开设“マッドハウス 動画訓練所”（MADHOUSE动画训练所）并着手招募学生，毕业通过考核后可进入MADHOUSE；同年10月成立50周年，宣布将于2023年1月举办纪念活动并开设特别官网，主题为“これからのマッドハウス”（未来的MADHOUSE）。


